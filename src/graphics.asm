init_graphics:
    ; load brick tiles definitions into vram (3 banks)
    ld hl, BRICK
    ld de, SCR2_CHARPATTERN+8
    ld bc, BRICK_END - BRICK
    call BIOS_LDIRVM
    ld hl, BRICK
    ld de, SCR2_CHARPATTERN+32*8*8+8
    ld bc, BRICK_END - BRICK
    call BIOS_LDIRVM
    ld hl, BRICK
    ld de, SCR2_CHARPATTERN+32*16*8+8
    ld bc, BRICK_END - BRICK
    call BIOS_LDIRVM

    ; brick colors
    ld hl, BRICK_COLORS
    ld de, SCR2_PIXELCOLOR+8
    ld bc, BRICK_COLORS_END - BRICK_COLORS
    call BIOS_LDIRVM
    ld hl, BRICK_COLORS
    ld de, SCR2_PIXELCOLOR+256*8+8
    ld bc, BRICK_COLORS_END - BRICK_COLORS
    call BIOS_LDIRVM
    ld hl, BRICK_COLORS
    ld de, SCR2_PIXELCOLOR+512*8+8
    ld bc, BRICK_COLORS_END - BRICK_COLORS
    call BIOS_LDIRVM

    ; load map
    ld hl, MAP
    ld de, SCR2_CHARPOS
    ld bc, MAP_END - MAP
    call BIOS_LDIRVM
    ret 
    

